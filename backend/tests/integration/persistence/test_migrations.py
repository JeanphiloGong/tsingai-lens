from __future__ import annotations

import os
from pathlib import Path

from alembic import command
from alembic.config import Config
from alembic.runtime.migration import MigrationContext
from alembic.script import ScriptDirectory
from alembic.util.exc import AutogenerateDiffsDetected
import pytest
from sqlalchemy import Column, Integer, Table, create_engine, inspect
from sqlalchemy.engine import URL, make_url

from infra.persistence.postgres.base import Base


BACKEND_ROOT = Path(__file__).resolve().parents[3]
HEAD_REVISION = "20260719_0004"


def test_empty_database_upgrades_checks_downgrades_and_upgrades_again(
    tmp_path,
) -> None:
    engine = create_engine(
        URL.create(
            "sqlite+pysqlite",
            database=str(tmp_path / "migration.sqlite"),
        )
    )
    config = Config(str(BACKEND_ROOT / "alembic.ini"))

    assert ScriptDirectory.from_config(config).get_current_head() == HEAD_REVISION

    try:
        with engine.begin() as connection:
            config.attributes["connection"] = connection

            assert MigrationContext.configure(connection).get_current_revision() is None

            command.upgrade(config, "head")

            assert (
                MigrationContext.configure(connection).get_current_revision()
                == HEAD_REVISION
            )
            assert inspect(connection).get_table_names() == [
                "alembic_version",
                "auth_sessions",
                "auth_users",
                "collection_files",
                "collection_handoffs",
                "collection_import_documents",
                "collection_imports",
                "collections",
                "stored_objects",
            ]
            command.check(config)

            command.downgrade(config, "base")

            assert MigrationContext.configure(connection).get_current_revision() is None
            assert inspect(connection).get_table_names() == ["alembic_version"]

            command.upgrade(config, "head")

            assert (
                MigrationContext.configure(connection).get_current_revision()
                == HEAD_REVISION
            )
    finally:
        engine.dispose()


def test_postgresql_migration_lifecycle_matches_models() -> None:
    database_url = os.getenv("LENS_TEST_DATABASE_URL")
    if not database_url:
        pytest.skip("LENS_TEST_DATABASE_URL is not configured")
    url = make_url(database_url)
    if url.drivername != "postgresql+psycopg" or not str(url.database).endswith(
        "_test"
    ):
        pytest.fail(
            "LENS_TEST_DATABASE_URL must use postgresql+psycopg and a *_test database"
        )

    engine = create_engine(url)
    config = Config(str(BACKEND_ROOT / "alembic.ini"))
    try:
        with engine.begin() as connection:
            config.attributes["connection"] = connection
            command.downgrade(config, "base")
            assert MigrationContext.configure(connection).get_current_revision() is None

            command.upgrade(config, "head")
            assert (
                MigrationContext.configure(connection).get_current_revision()
                == HEAD_REVISION
            )
            assert set(inspect(connection).get_table_names()) == {
                "alembic_version",
                "auth_sessions",
                "auth_users",
                "collection_files",
                "collection_handoffs",
                "collection_import_documents",
                "collection_imports",
                "collections",
                "stored_objects",
            }
            command.check(config)

            command.downgrade(config, "base")
            assert MigrationContext.configure(connection).get_current_revision() is None
            assert inspect(connection).get_table_names() == ["alembic_version"]

            command.upgrade(config, "head")
            assert (
                MigrationContext.configure(connection).get_current_revision()
                == HEAD_REVISION
            )
    finally:
        with engine.begin() as connection:
            config.attributes["connection"] = connection
            command.downgrade(config, "base")
        engine.dispose()


def test_alembic_check_rejects_model_drift(tmp_path) -> None:
    engine = create_engine(
        URL.create(
            "sqlite+pysqlite",
            database=str(tmp_path / "drift.sqlite"),
        )
    )
    config = Config(str(BACKEND_ROOT / "alembic.ini"))
    drift_table = Table(
        "unmigrated_example",
        Base.metadata,
        Column("id", Integer, primary_key=True),
    )

    assert drift_table.primary_key.name == "pk_unmigrated_example"

    try:
        with engine.begin() as connection:
            config.attributes["connection"] = connection
            command.upgrade(config, "head")

            with pytest.raises(
                AutogenerateDiffsDetected,
                match="New upgrade operations detected",
            ):
                command.check(config)
    finally:
        Base.metadata.remove(drift_table)
        engine.dispose()
